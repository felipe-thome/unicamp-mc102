# Cria , transforma em inteiros e armazena variáveis arbitrárias
a = int(input())
b = int(input())

# Imprime as variáveis
print("a =", a)
print("b =", b)

# Imprime a soma
print("a + b =", a + b)
# Imprime a diferença
print("a - b =", a - b)
# Imprime o produto
print("a * b =", a * b)
# Imprime a potência a^(b)
print("a ** b =", a ** b)
# Imprime o quociente
print("a // b =", a // b)
# Imprime o resto do quociente
print("a % b =", a % b)